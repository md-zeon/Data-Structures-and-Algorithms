public class JavaBasics {
    public static void main(String args[]) {
        // This is a single-line comment
        // Boilerplate Java program
        System.out.print("This is a basic Java program.");
        // It prints "Hello, World!" to the console
        System.out.println("Hello, World!");
        // It prints "Hello, World!" to the console
        System.out.println("This is a basic Java program.");
        // It prints "This is a basic Java program." to the console



        // The main method is the entry point of the program
        // System.out.print is used to print text to the console without a newline at the end
        // System.out.println is used to print text to the console with a newline at the end
        // ; is used to terminate statements in Java

        System.out.println("Hello,\nWorld!");
    }
}
